"""
compare dois numeros inteiro e mostre o maior ou igual
"""
numero1 = int(input('  Digite o primeiro número : '))
numero2 = int(input('  Digite o segundo número : '))
if numero1 > numero2:
    print(
        f' O primeiro numero {numero1} é maior que o segundo número {numero2}'
    )
elif numero1 < numero2:
    print(
        f'  O segundo número {numero2} é maior que o primeiro número {numero1}'
    )
else:
    print(f'  O primeiro e segundo numeros são iguais')
